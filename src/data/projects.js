export function getProjectPath(slug) {
  return `/projects/${slug}`;
}

export const PROJECTS_BY_ID = Object.fromEntries(
  PROJECT_REGISTRY.map((project) => [project.id, project])
);

export const PROJECTS_BY_SLUG = Object.fromEntries(
  PROJECT_REGISTRY.map((project) => [project.slug, project])
);

export function getProjectById(id) {
  return PROJECTS_BY_ID[id] ?? null;
}

export function getProjectBySlug(slug) {
  return PROJECTS_BY_SLUG[slug] ?? null;
}
